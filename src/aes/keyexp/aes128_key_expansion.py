Nb = 4
Nk = 4
Nr = 10


def rot_word(word: list[int]) -> list[int]:
    """
    ROTWORD([a0, a1, a2, a3]) = [a1, a2, a3, a0]
    (FIPS 197 Section 5.2, Eq. 5.10)
    """
    return word[1:] + word[:1]


def sub_word(word: list[int]) -> list[int]:
    """
    SUBWORD applies S-box to each of the four bytes of the word.
    (FIPS 197 Section 5.2, Eq. 5.11)
    """
    return [SBOX[b] for b in word]


def xor_words(a: list[int], b: list[int]) -> list[int]:
    """XOR two words (lists of 4 bytes) byte by byte."""
    return [x ^ y for x, y in zip(a, b)]


def key_expansion(key: list[int]) -> list[list[int]]:
    """
    KEYEXPANSION for AES-128 (FIPS 197 Algorithm 2).
    key: 16 bytes (128 bits), as list of integers 0..255
    Returns: w[0..43], each w[i] is a list of 4 bytes (a word).
    """
    assert len(key) == 16, "AES-128 key must be 16 bytes"
    w: list[list[int]] = []

    # First Nk words of the expanded key are the key itself.
    for i in range(Nk):
        w.append(key[4 * i : 4 * i + 4])

    # Generate the remaining words.
    i = Nk
    while i < 4 * (Nr + 1):
        temp = w[i - 1][:]
        if i % Nk == 0:
            temp = xor_words(sub_word(rot_word(temp)), RCON[(i // Nk) - 1])
        elif Nk > 6 and i % Nk == 4:
            temp = sub_word(temp)
        w.append(xor_words(w[i - Nk], temp))
        i += 1

    return w


def word_to_hex(word: list[int]) -> str:
    """Format a word (4 bytes) as 8 hex characters, e.g. '2b7e1516'."""
    return "".join(f"{b:02x}" for b in word)


